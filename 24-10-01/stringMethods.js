// toUpperCase()
const greeting = "hello";
console.log("toUpperCase():", greeting.toUpperCase()); // "HELLO"

// split()
const sentence = "Hello world";
const words = sentence.split(" ");
console.log("split():", words); // ["Hello", "world"]

// includes()
const sentence2 = "JavaScript is fun";
console.log("includes():", sentence2.includes("fun")); // true

// replace()
const newSentence = sentence2.replace("JavaScript", "coding");
console.log("replace():", newSentence); // "coding is fun"
